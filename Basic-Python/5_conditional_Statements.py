# Code for checking whether a number is zero, positive, or negative

def number1(num):
    if(num == 0):
        print("Number is Zero")  # If the number is 0, print "Number is Zero"
    elif(num > 0):
        print("Number is positive")  # If the number is greater than 0, print "Number is positive"
    else:
        print("Number is negative")  # If the number is less than 0, print "Number is negative"

number1(12)  # Calling the number1 function with argument 12
